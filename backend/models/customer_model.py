class Customer:
    def __init__(self, id=None, name=None, email=None, phone=None, address=None, item_name=None, qty=None, registered_date=None):
        self.id = id
        self.name = name
        self.email = email
        self.phone = phone
        self.address = address
        self.item_name = item_name
        self.qty = qty
        self.registered_date = registered_date