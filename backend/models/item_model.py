class Item:
    def __init__(self, id=None, name=None, category=None, price=None, quantity=None, unit=None, description=None, supplier=None, status=None, added_date=None):
        self.id = id
        self.name = name
        self.category = category
        self.price = price
        self.quantity = quantity
        self.unit = unit
        self.description = description
        self.supplier = supplier
        self.status = status
        self.added_date = added_date
